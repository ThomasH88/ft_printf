#include "ft_printf.h"

void	print_bits(unsigned char byte)
{
	unsigned int	i;

	i = 256;
	while ((i >>= 1) > 8)
		(byte & i) ? write(1, "1", 1) : write(1, "0", 1);
	write(1, " ", 1);
	i <<= 1;
	while (i >>= 1)
		(byte & i) ? write(1, "1", 1) : write(1, "0", 1);
	write(1, "\n", 1);
}
